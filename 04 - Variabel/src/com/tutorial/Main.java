package com.tutorial;

public class Main {
    public static void main(String[] args){
        // variabel
        int b = 20; // b adalah variabel, isi dari variabel b adalah 20
        // output: nilai b = 20

        // assigntment
        // assignment artinya dengan tanda '=' kita mengintruksikan mesin untuk mengisikan sebuah nilai.
        int z = 15;
        String nama = "Hadzik";
        System.out.println("nilai z = " + z);
        System.out.println("nama = " + nama);

        // deklarasi
        int angka; /* deklarasi adalah penetapan suatu variabel beserta tipe datanya, namun tidak memiliki nilai
                      umumnya variabel yang di deklarasikan, nantinya diisi dengan memanggil nama variabel nya*/

        angka = 100;

    }
}
